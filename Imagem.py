class Imagem:
  def __init__(self, nomeO, diretorioO, pastaO, nomeM, diretorioM, pastaM, treino):
    self.nomeO = nomeO
    self.diretorioO = diretorioO        # O significa Original
    self.pastaO = pastaO
    self.nomeM = nomeM                  # M significa Modificado
    self.diretorioM = diretorioM
    self.pastaM = pastaM
    self.treino = treino